print('Programa que calcula la discriminante de una ecuacion de segundo grado')

a = float(input('Ingrese la variable a de la ecuacion \t'))

b = float(input('Ingrese la variable b de la ecuacion \t'))

c = float(input('Ingrese la variable c de la ecuacion \t'))

discriminante = (b ** 2) - 4 * a * c

print('La discriminante de la ecuacion es de', discriminante )