print('Programa que calcula el perimetro y area de un rectangulo')

ancho = float(input('Ingrese el ancho del rectangulo \t'))

altura = float(input('Ingrese la altura del rectangulo \t'))

perimetro = 2 * (ancho + altura)

area = ancho * altura

print("El area del rectangulo es de", area ,"y su perimetro es de", perimetro)